#!/usr/bin/env node
/**
 * arch-map-parity.js — MAPGEN-GUARD-006.
 *
 * JS-side sibling of architecture_map_generator.py's extract_from_html() (that
 * function's regex anchors, lines ~200-253, ported here — read-only in both
 * directions, this script never writes to the target HTML or arch-map-data.json).
 *
 * Purpose: catch the build artifact (resources/sasmaster-true-architecture-interactive.html)
 * drifting from the data it was built from — either because someone hand-edited a
 * number back into the HTML, or hand-restored a pre-build copy that never went
 * through build-arch-map.js at all.
 *
 * Two independent checks, in this order:
 *   1. GENERATED-banner check — the HTML must start with (or contain near the top)
 *      the '<!-- GENERATED by build-arch-map.js ... -->' banner build-arch-map.js
 *      stamps on every real build. Missing banner = someone hand-restored/hand-
 *      authored this file outside the build step. Exit 2.
 *   2. Number-parity check — re-extracts every number this script knows how to
 *      anchor (crontab_lines, launchd_on_disk/loaded, gha_workflows_total,
 *      md_schema_count, md_ref_skill_routing_rows, md_ref_doctrine_conformance_rows,
 *      agents_array_len/live_type/subagent_type, agents_summary_total/live_total/
 *      by_type_live/by_type_subagent, fleet_dir_entries, fleet_js_count,
 *      resources_json_count) from the HTML prose and diffs each against the
 *      corresponding value currently stored in arch-map-data.json's counts[key].value.
 *      Any disagreement = exit 1, listing every mismatched key with both values.
 *
 * Exit codes:
 *   0 — banner present, every extractable key matches arch-map-data.json
 *   1 — banner present, but >=1 key disagrees (lists them)
 *   2 — GENERATED banner missing (treat as a hand-restored/pre-build file; number
 *       parity is not even attempted, since a file that failed the build gate has
 *       no assurance its numbers were ever resolved against real data)
 *
 * Usage:
 *   node scripts/ci/arch-map-parity.js
 *   node scripts/ci/arch-map-parity.js --html /path/to/file.html --data /path/to/arch-map-data.json
 *   node scripts/ci/arch-map-parity.js --json
 *
 * Status: dormant by default — no cron/CI/hook currently calls this script.
 * MAPGEN-DRIFT-004's architecture_map_drift_check.py shells out to it (best-effort,
 * merged into its existing html_drift list) so there is one consolidated rot
 * surface, but that call is itself report-only/non-blocking — see that script.
 */
'use strict';

const fs = require('fs');
const path = require('path');

function parseArgs(argv) {
  const out = { json: false };
  for (let i = 0; i < argv.length; i++) {
    const a = argv[i];
    if (a === '--html') out.html = argv[++i];
    else if (a === '--data') out.data = argv[++i];
    else if (a === '--json') out.json = true;
  }
  return out;
}

const args = parseArgs(process.argv.slice(2));
const STATUS_ROOT = path.resolve(__dirname, '..', '..');
const HTML_PATH = args.html || path.join(STATUS_ROOT, 'resources', 'sasmaster-true-architecture-interactive.html');
const DATA_PATH = args.data || path.join(STATUS_ROOT, 'resources', 'arch-map-data.json');

const BANNER_RE = /<!--\s*GENERATED by build-arch-map\.js/;

// ── Port of architecture_map_generator.py's extract_from_html() regex anchors ──
function extractFromHtml(html) {
  const found = {};
  let m;

  if ((m = html.match(/(\d+)\s+crontab/))) found.crontab_lines = parseInt(m[1], 10);

  if ((m = html.match(/(\d+)\s+launchd/))) {
    found.launchd_on_disk = parseInt(m[1], 10);
    found.launchd_loaded = parseInt(m[1], 10); // map claims parity live, same as the Python original
  }

  if ((m = html.match(/(\d+)\s+workflows\s*\(live count/))) found.gha_workflows_total = parseInt(m[1], 10);

  if ((m = html.match(/(\d+)\s+schemas\s*\(live count/))) found.md_schema_count = parseInt(m[1], 10);

  if ((m = html.match(/(\d+)\s+routing rules\s*\(ref\.skill_routing/))) found.md_ref_skill_routing_rows = parseInt(m[1], 10);

  if ((m = html.match(/(\d+)\s+doctrine rows\s*\(ref\.doctrine_conformance/))) found.md_ref_doctrine_conformance_rows = parseInt(m[1], 10);

  if ((m = html.match(/actual agents array in the same file has (\d+) entries \((\d+) live-type\+(\d+) subagent-type\)/))) {
    found.agents_array_len = parseInt(m[1], 10);
    found.agents_array_live_type = parseInt(m[2], 10);
    found.agents_array_subagent_type = parseInt(m[3], 10);
  }

  if ((m = html.match(/summary block says (\d+) total\/(\d+) live_total \((\d+) live-type\+(\d+) subagent-type\)/))) {
    found.agents_summary_total = parseInt(m[1], 10);
    found.agents_summary_live_total = parseInt(m[2], 10);
    found.agents_summary_by_type_live = parseInt(m[3], 10);
    found.agents_summary_by_type_subagent = parseInt(m[4], 10);
  }

  if ((m = html.match(/(\d+)\s+dir entries\/(\d+)\s+js/))) {
    found.fleet_dir_entries = parseInt(m[1], 10);
    found.fleet_js_count = parseInt(m[2], 10);
  }

  if ((m = html.match(/(\d+)\s+resources\s*\(live count, resources\.json/))) found.resources_json_count = parseInt(m[1], 10);

  return found;
}

function fail(code, msg) {
  console.error(`arch-map-parity: FAIL (exit ${code}) — ${msg}`);
  process.exit(code);
}

function main() {
  if (!fs.existsSync(HTML_PATH)) fail(2, `target HTML not found: ${HTML_PATH}`);
  const html = fs.readFileSync(HTML_PATH, 'utf8');

  if (!BANNER_RE.test(html)) {
    const report = { html: HTML_PATH, banner_present: false, mismatches: [] };
    if (args.json) console.log(JSON.stringify(report, null, 2));
    fail(2, `GENERATED banner not found in ${HTML_PATH} — this file was hand-restored or hand-authored outside build-arch-map.js. Rebuild via 'npm run build:archmap'.`);
  }

  if (!fs.existsSync(DATA_PATH)) fail(2, `arch-map-data.json not found: ${DATA_PATH} — run arch_map_census.sh --emit first`);
  let data;
  try {
    data = JSON.parse(fs.readFileSync(DATA_PATH, 'utf8'));
  } catch (e) {
    fail(2, `arch-map-data.json is not valid JSON: ${e.message}`);
  }
  const counts = (data && data.counts) || {};

  const parsed = extractFromHtml(html);
  const mismatches = [];
  for (const [key, htmlVal] of Object.entries(parsed)) {
    const entry = counts[key];
    if (!entry || !Object.prototype.hasOwnProperty.call(entry, 'value')) {
      mismatches.push({ key, html_value: htmlVal, json_value: undefined, note: 'key not present in arch-map-data.json counts' });
      continue;
    }
    if (entry.value !== htmlVal) {
      mismatches.push({ key, html_value: htmlVal, json_value: entry.value });
    }
  }

  const report = {
    html: HTML_PATH,
    data: DATA_PATH,
    banner_present: true,
    extracted_keys: Object.keys(parsed).length,
    mismatches,
  };

  if (mismatches.length) {
    if (args.json) {
      console.log(JSON.stringify(report, null, 2));
    } else {
      console.error('arch-map-parity: FAIL (exit 1) — HTML/JSON disagreement on:');
      for (const m of mismatches) {
        console.error(`  ${m.key}: html=${JSON.stringify(m.html_value)} json=${JSON.stringify(m.json_value)}${m.note ? ' (' + m.note + ')' : ''}`);
      }
    }
    process.exit(1);
  }

  if (args.json) {
    console.log(JSON.stringify(report, null, 2));
  } else {
    console.log(`arch-map-parity: PASS — banner present, ${report.extracted_keys} key(s) extracted, all match arch-map-data.json.`);
  }
  process.exit(0);
}

main();
